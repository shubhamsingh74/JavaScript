// booleans & comparison operator 

// booleans 
// true, false 

let num1 = 7;
let num2 = "7";

// < , > , <= , >= 
console.log(num1<num2);

// == vs === 
// == it only checks the value 
// === its checks the datatypes with value
console.log(num1 === num2);



// != vs !==
// != it only checks the value
// !== it checks both datatype and value also
console.log(num1 !== num2);
